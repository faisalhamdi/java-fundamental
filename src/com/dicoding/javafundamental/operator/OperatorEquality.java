package com.dicoding.javafundamental.operator;

public class OperatorEquality {

    public static void main(String[] x) {

        int value1 = 5;
        int value2 = 4;
        boolean hasil;
        hasil = value1 == value2;

        System.out.println("Hasil 'value1 == value2' adalah " + hasil);

        hasil = value1 != value2;
        System.out.println("Hasil 'value1 != value2' adalah " + hasil);

    }
}
